public class StringContentEq {
    public static void main(String[] args) {
        String s1="jelly fish";
        System.out.println(s1.contentEquals("jelly fish"));

        String s2="abc";
        System.out.println(s2.equals("abc"));

        String s3="i like eating icecream";
        System.out.println(s3.contains("class"));
    }
}
